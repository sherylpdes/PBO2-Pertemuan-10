    public final void loadData() {
        btnsave.setEnabled(true);
        btnedit.setEnabled(false);
        btndelete.setEnabled(false);
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        try {
            Connection c = Koneksi.getKoneksi();
            Statement s = c.createStatement();
            String sql = "SELECT * FROM tbl_dokter";
            ResultSet r = s.executeQuery(sql);
            while (r.next()) {
                Object[] o = new Object[7];
                o[0] = r.getString("kode_dokter");
                o[1] = r.getString("nama_dokter");
                o[2] = r.getString("no_str");
                o[3] = r.getString("no_tlp");
                o[4] = r.getString("jenis_kelamin");
                o[5] = r.getString("alamat_dokter");
                o[6] = r.getString("kode_poli");
                model.addRow(o);
            }
            r.close();
            s.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Terjadi Error pada Data Dokter");
        }
    }
    
    private void kode() {
        try {
            Connection c = Koneksi.getKoneksi();
            Statement s = c.createStatement();
            String sql = "SELECT * FROM tbl_dokter ORDER by kode_dokter desc";
            ResultSet r = s.executeQuery(sql);
            if (r.next()) {
                String kodedokter = r.getString("kode_dokter").substring(1);
                String Akhir = "" + (Integer.parseInt(kodedokter) + 1);
                String Nol = "";
                if (Akhir.length() == 1) {
                    Nol = "0000";
                } else if (Akhir.length() == 2) {
                    Nol = "000";
                } else if (Akhir.length() == 3) {
                    Nol = "00";
                } else if (Akhir.length() == 3) {
                    Nol = "0";
                } else if (Akhir.length() == 4) {
                    Nol = "";
                }
                txtkodedokter.setText("D" + Nol + Akhir);
            } else {
                txtkodedokter.setText("D00001");
            }
        }catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan pada Kode Dokter");
        }
    }
    
