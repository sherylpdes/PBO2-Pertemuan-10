    private void tbldokterMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbldokterMouseClicked
        // TODO add your handling code here:
        btnsave.setEnabled(false);
        btnedit.setEnabled(true);
        btndelete.setEnabled(true);
        int i = tbldokter.getSelectedRow();
        if (i == -1) {
            return;
        }
        String kode = (String) model.getValueAt(i, 0);
        txtkodedokter.setText(kode);
        txtkodedokter.setEnabled(false);
        String nama = (String) model.getValueAt(i, 1);
        txtnama.setText(nama);
        String str = (String) model.getValueAt(i, 2);
        txtstr.setText(str);
        String notlp = (String) model.getValueAt(i, 3);
        txtnotelp.setText(notlp);
        String jeniskelamin = (String) model.getValueAt(i, 4);
        if (jeniskelamin.equals("Laki-Laki")) {
            rbl.setSelected(true);
        } else if (jeniskelamin.equals("Perempuan")) {
            rbp.setSelected(true);
        }
        String alamat = (String) model.getValueAt(i, 5);
        txtalamat.setText(alamat);
        String poli = (String) model.getValueAt(i, 6);
        cbkodepoli.setSelectedItem(poli);
        
        btnsave.setEnabled(false);
    }//GEN-LAST:event_tbldokterMouseClicked

    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        // TODO add your handling code here:
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        try {
            Connection c = Koneksi.getKoneksi();
            Statement s = c.createStatement();
            String sql = "select * from tbl_dokter where kode_dokter like '%" + txtsearch.getText() + 
                    "%' or nama_dokter like'%" + txtsearch.getText() + "%'or no_str like'" + txtsearch.getText() + 
                    "%' or no_tlp like'%" + txtsearch.getText() + "%' or jenis_kelamin like'" + txtsearch.getText()
                    + "%' or alamat_dokter like'" + txtsearch.getText() + "%' or kode_poli like'" + txtsearch.getText() + "%'";
            ResultSet r = s.executeQuery(sql);
            while (r.next()) {
                Object[] o = new Object[7];
                o[0] = r.getString("kode_dokter");
                o[1] = r.getString("nama_dokter");
                o[2] = r.getString("no_str");
                o[3] = r.getString("no_tlp");
                o[4] = r.getString("jenis_kelamin");
                o[5] = r.getString("alamat_dokter");
                o[6] = r.getString("kode_poli");
                model.addRow(o);
            }
            r.close();
            s.close();
        } catch (SQLException e) {
            System.out.println("Terjadi Error");
        } 
    }//GEN-LAST:event_btnsearchActionPerformed

