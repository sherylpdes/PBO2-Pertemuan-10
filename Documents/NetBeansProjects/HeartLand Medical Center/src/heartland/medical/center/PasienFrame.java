    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        String rm = txtnorm.getText();
        String nama = txtnama.getText();
        String umur = txtumur.getText();
        String alamat = txtalamat.getText();
        String nomortelepon = txtnotlp.getText();
        String jeniskelamin = rbl.isSelected() ? "L" : rbp.isSelected() ? "P" : "-";
        String tanggal = date.getDate()!= null ? new SimpleDateFormat("yyyy-MM-dd").format(date.getDate()) : "-";
        if(rm.equals("") || nama.equals("") || umur.equals("") || alamat.equals("")|| nomortelepon.equals("") 
                || jeniskelamin.equals("") || tanggal.equals("")){
            JOptionPane.showMessageDialog(null, "LENGKAPI DATA !", "Pasien", JOptionPane.INFORMATION_MESSAGE);
        }else{
            try {
                Connection c = Koneksi.getKoneksi();
                if (c == null) {
                    throw new SQLException("Failed to get database connection.");
                }
                String sql = "INSERT INTO tbl_pasien VALUES (?, ?, ?, ?, ?, ?, ?)";
                PreparedStatement p = c.prepareStatement(sql);
                p.setString(1, rm);
                p.setString(2, nama);
                p.setString(3, umur);
                p.setString(4, alamat);
                p.setString(5, tanggal);
                p.setString(6, jeniskelamin);
                p.setString(7, nomortelepon);
                p.executeUpdate();
                p.close(); 
            } catch (SQLException e) {
                System.out.println("Terjadi Error" + e.getMessage());
            } finally {
            loadData();
            clean();
            kode();
            JOptionPane.showMessageDialog(null, "Data berhasil tersimpan", "Pasien", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnsaveActionPerformed

