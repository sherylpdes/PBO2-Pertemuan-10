    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        // TODO add your handling code here:
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        try {
            Connection c = Koneksi.getKoneksi();
            Statement s = c.createStatement();
            String sql = "select * from tbl_pasien where no_rm like '%" + txtsearch.getText() + 
                    "%' or nama_pasien like'%" + txtsearch.getText() + "%'or umur_pasien like'" + txtsearch.getText() + 
                    "%' or alamat_pasien like'%" + txtsearch.getText() + "%' or tgl_lahir like'" + txtsearch.getText()
                    + "%' or jenis_kelamin like'" + txtsearch.getText() + "%' or no_tlp like'" + txtsearch.getText() + "%'";
            ResultSet r = s.executeQuery(sql);
            while (r.next()) {
                Object[] o = new Object[7];
                o[0] = r.getString("no_rm");
                o[1] = r.getString("nama_pasien");
                o[2] = r.getString("umur_pasien");
                o[3] = r.getString("alamat_pasien");
                o[4] = r.getString("tgl_lahir");
                o[5] = r.getString("jenis_kelamin");
                o[6] = r.getString("no_tlp");
                model.addRow(o);
            }
            r.close();
            s.close();
        } catch (SQLException e) {
            System.out.println("Terjadi Error");
        } 
    }//GEN-LAST:event_btnsearchActionPerformed

