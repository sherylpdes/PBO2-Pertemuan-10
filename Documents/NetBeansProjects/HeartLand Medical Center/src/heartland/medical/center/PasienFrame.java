    public final void loadData() {
        btnsave.setEnabled(true);
        btnedit.setEnabled(false);
        btndelete.setEnabled(false);
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        try {
            Connection c = Koneksi.getKoneksi();
            Statement s = c.createStatement();
            String sql = "SELECT * FROM tbl_pasien";
            ResultSet r = s.executeQuery(sql);
            while (r.next()) {
                Object[] o = new Object[7];
                o[0] = r.getString("no_rm");
                o[1] = r.getString("nama_pasien");
                o[2] = r.getString("umur_pasien");
                o[3] = r.getString("alamat_pasien");
                o[4] = r.getString("tgl_lahir");
                o[5] = r.getString("jenis_kelamin");
                o[6] = r.getString("no_tlp");
                model.addRow(o);
            }
            r.close();
            s.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Terjadi Error pada Data Pasien");
        }
    }
    
    private void kode() {
        try {
            Connection c = Koneksi.getKoneksi();
            Statement s = c.createStatement();
            String sql = "SELECT * FROM tbl_pasien ORDER by no_rm desc";
            ResultSet r = s.executeQuery(sql);
            if (r.next()) {
                String norm = r.getString("no_rm").substring(1);
                String Akhir = "" + (Integer.parseInt(norm) + 1);
                String Nol = "";
                if (Akhir.length() == 1) {
                    Nol = "0000";
                } else if (Akhir.length() == 2) {
                    Nol = "000";
                } else if (Akhir.length() == 3) {
                    Nol = "00";
                } else if (Akhir.length() == 3) {
                    Nol = "0";
                } else if (Akhir.length() == 4) {
                    Nol = "";
                }
                txtnorm.setText("R" + Nol + Akhir);
            } else {
                txtnorm.setText("R00001");
            }
        }catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan pada Nomor Rekam Medis");
        }
    }
    
